#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			is_neg;
	long int	res;

	if (!*str)
		return (0);
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		is_neg = -1;
	else
		is_neg = 1;
	if (is_neg == -1 || str[i] == '+')
		i++;
	res = 0;
	while (str[i] >= '0' && str[i] <= '9')
		res = (res * 10) + (str[i++] - '0');
	if (res > 2147483648 && is_neg == -1)
		return (0);
	if (res > 2147483648 && is_neg == 1)
		return (-1);
	return ((int)res * is_neg);
}
